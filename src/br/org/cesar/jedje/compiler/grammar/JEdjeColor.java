package br.org.cesar.jedje.compiler.grammar;

public class JEdjeColor {

	private int r;
	private int g;
	private int b;
	private int a;
	
	public JEdjeColor(int _r, int _g, int _b, int _a) {
		this.r = _r;
		this.g = _g;
		this.b = _b;
		this.a = _a;
	}

	/**
	 * @return the r
	 */
	public int getR() {
		return r;
	}

	/**
	 * @return the g
	 */
	public int getG() {
		return g;
	}

	/**
	 * @return the b
	 */
	public int getB() {
		return b;
	}

	/**
	 * @return the a
	 */
	public int getA() {
		return a;
	}
	
}
